class Solution:
    def isPalindrome(self, x: int) -> bool:
        # 罕见的log_10(N)算法
        if x < 0 or ((x % 10) == 0 and x != 0):return False
        revertedNumber = 0
        while x > revertedNumber:
            revertedNumber = revertedNumber * 10 + x % 10
            # 得到除去最低位的数
            x //= 10
